package nanocovax;

public class NguoiQuanLy {
    String id, phanquyen, tinhtrang;

    public String getId() {
        return id;
    }
    public String getPhanQuyen() {
        return phanquyen;
    }
    public String getTinhtrang() {
        return tinhtrang;
    }

    public void setId(String srcId) {
        id = srcId;
    }
    public void setPhanquyen(String srcPhanQuyen) {
        phanquyen = srcPhanQuyen;
    }
    public void setTinhtrang(String srcTinhTrang) {
        tinhtrang = srcTinhTrang;
    }
}
