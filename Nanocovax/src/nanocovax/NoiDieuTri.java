package nanocovax;

public class NoiDieuTri {
    String id;
    String ten;
    int sucChua, dangChua;

    public NoiDieuTri() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getSucChua() {
        return sucChua;
    }

    public void setSucChua(int sucChua) {
        this.sucChua = sucChua;
    }

    public int getDangChua() {
        return dangChua;
    }

    public void setDangChua(int dangChua) {
        this.dangChua = dangChua;
    }
}
